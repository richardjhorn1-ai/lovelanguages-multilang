/**
 * Get UI translations for a specific language, with English fallback
 */
export function getUITranslations(langCode: string): UITranslations {
  return UI_TRANSLATIONS[langCode] || UI_TRANSLATIONS.en;
}

/**
 * Replace {language} placeholder in a string
 */
export function insertLanguage(text: string, languageName: string): string {
  return text.replace(/{language}/g, languageName);
}
