// Reverse index: name -> dates
export function buildNameToDateIndex(): Map<string, string[]> {
  const index = new Map<string, string[]>();

  for (const [date, names] of Object.entries(polishNameDays)) {
    for (const name of names) {
      const normalizedName = name.toLowerCase();
      if (!index.has(normalizedName)) {
        index.set(normalizedName, []);
      }
      index.get(normalizedName)!.push(date);
    }
  }

  return index;
}

// Format date as readable string
export function formatNameDayDate(dateKey: string): string {
  const [month, day] = dateKey.split('-').map(Number);
  const months = [
    'January', 'February', 'March', 'April', 'May', 'June',
    'July', 'August', 'September', 'October', 'November', 'December'
  ];
  return `${months[month - 1]} ${day}`;
}

// Get next upcoming name day
export function getNextNameDay(name: string): { date: string; formatted: string; daysUntil: number } | null {
  const index = buildNameToDateIndex();
  const normalizedName = name.toLowerCase();
  const dates = index.get(normalizedName);

  if (!dates || dates.length === 0) return null;

  const today = new Date();
  const currentYear = today.getFullYear();

  // Find the next upcoming date
  let nextDate: Date | null = null;
  let nextDateKey = '';

  for (const dateKey of dates) {
    const [month, day] = dateKey.split('-').map(Number);
    let date = new Date(currentYear, month - 1, day);

    // If the date has passed this year, use next year
    if (date < today) {
      date = new Date(currentYear + 1, month - 1, day);
    }

    if (!nextDate || date < nextDate) {
      nextDate = date;
      nextDateKey = dateKey;
    }
  }

  if (!nextDate) return null;

  const daysUntil = Math.ceil((nextDate.getTime() - today.getTime()) / (1000 * 60 * 60 * 24));

  return {
    date: nextDateKey,
    formatted: formatNameDayDate(nextDateKey),
    daysUntil
  };
}

