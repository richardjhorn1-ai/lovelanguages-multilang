// Helper to get phrases by difficulty
export const getPhrasesByDifficulty = (difficulty: RomanticPhrase['difficulty']) =>
  ROMANTIC_PHRASES.filter(p => p.difficulty === difficulty);

// Helper to get random phrases
export const getRandomPhrases = (count: number, difficulty?: RomanticPhrase['difficulty']) => {
  const pool = difficulty ? getPhrasesByDifficulty(difficulty) : ROMANTIC_PHRASES;
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};
